    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
       $area_input = new Area();

       $area_input->areaid = Uuid::uuid();
        $area_input->name = $request->name;
        $area_input->size = $request->area_size;
        $area_input->address = $request->area_address;
        $area_input->updated_by = $request->username;
        $area_input->save();

        return redirect('/areaList');
    }

